import 'dart:convert';

class Article {
  final String by;
  final int descendants;
  final int id;
  final List<int> kids;
  final int score;
  final DateTime time;
  final String title;
  final String type;
  final String url;

  Article(
      {this.by,
      this.descendants,
      this.id,
      this.kids,
      this.score,
      this.time,
      this.title,
      this.type,
      this.url});

  factory Article.fromJson(String jsonString) {
    if (jsonString == null)
      return null;
    else {
      Map<String, dynamic> json = jsonDecode(jsonString);
      return Article(
        by: json['by'] ?? 'null',
        descendants: json['descendants'] ?? 0,
        id: json['id'] ?? 0,
        kids: json['kids'] ?? [null],
        score: json['score'] ?? 0,
        time: json['time'] ?? DateTime.now(),
        title: json['title'] ?? 'null',
        type: json['type'] ?? 'null',
        url: json['url'] ?? 'null',
      );
    }
  }
}

